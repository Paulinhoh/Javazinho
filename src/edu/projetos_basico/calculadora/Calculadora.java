package edu.projetos_basico.calculadora;

public class Calculadora {
    public double soma(double valorA, double valorB) {
        return valorA + valorB;
    }

    public double sub(double valorA, double valorB) {
        return valorA - valorB;
    }

    public double mult(double valorA, double valorB) {
        return valorA * valorB;
    }

    public double div(double valorA, double valorB) {
        return valorA / valorB;
    }
}
